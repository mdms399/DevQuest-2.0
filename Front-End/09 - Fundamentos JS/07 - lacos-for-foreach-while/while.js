/*
A sintaxe do `while` tem 2 partes:

1) **Condicao**: até quando o loop deve acontencer
2) **Bloco de código**: o que será repetido

while(codicao) {

}

*/

let numero1 = 0
const numero2 = 10

while(numero1 < numero2) {
    console.log(`Incrementando a variável numero1: ${numero1}`);
    numero1++
}