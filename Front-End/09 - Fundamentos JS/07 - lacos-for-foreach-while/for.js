/*
A sintaxe do `for` tem 3 partes:

1. **Inicializador**: onde começa a contagem (geralmente `0`)
2. **Condição**: até quando o loop deve acontecer
3. **Incremento**: aumenta +1 a cada volta

for (inicializador; condicao; incremento) {

}

*/

for(let i = 0; i <= 5; i++) {
    console.log(`Incrementando a variável i para ${i}`)
}