const numero1 = 3
const numero2 = "3"

console.log(numero1 == numero2);

// === - operador de identico, levar em consideracao tanto o tipo quanto o valor

const numero3 = 3
const numero4 = "3"

console.log(numero3 === numero4);

// !== - nao identico, testa se as variaveis sao diferentes em valor e tipo

const numero5 = 3
const numero6 = 3

console.log(numero5 !== numero6);

// != - nao igual, testa se as variaveis sao diferntes em valor, sem considerar o tipo

const numero7 = 3
const numero8 = "3"

console.log(numero7 != numero8);

